/**
 * 겉보기 달 황경 (도, 0~360)
 * Meeus 47장 주기항 59개 + 섭동 보정항(A1: 금성, A2: 목성, L'−F: 장동성 항) + 장동 보정.
 * 오차 ≈ ±10″ ≈ ±0.003°
 */
export function moonLongitude(jd: number): number {
  const T = (jd - 2451545.0) / 36525;
  const T2 = T * T;
  const T3 = T2 * T;
  const T4 = T3 * T;

  // 달 평균 황경 L'
  const Lp = norm360(218.3164477 + 481267.88123421 * T - 0.0015786 * T2 + T3 / 538841 - T4 / 65194000);
  // 평균 이각 D
  const D = norm360(297.8501921 + 445267.1114034 * T - 0.0018819 * T2 + T3 / 545868 - T4 / 113065000);
  // 태양 평균 근점이각 M
  const M = norm360(357.5291092 + 35999.0502909 * T - 0.0001536 * T2 + T3 / 24490000);
  // 달 평균 근점이각 M'
  const Mp = norm360(134.9633964 + 477198.8675055 * T + 0.0087414 * T2 + T3 / 69699 - T4 / 14712000);
  // 위도 인수 F
  const F = norm360(93.272095 + 483202.0175233 * T - 0.0036539 * T2 - T3 / 3526000 + T4 / 863310000);

  // 지구 궤도 이심률 감소 보정 계수 (M 포함 항에 적용)
  const E = 1 - 0.002516 * T - 0.0000074 * T2;
  const E2 = E * E;

  let sumL = 0; // 단위: 10⁻⁶ °
  for (const [d, m, mp, f, coeff] of MOON_LON_TERMS) {
    let term = coeff * Math.sin((d * D + m * M + mp * Mp + f * F) * DEG);
    if (m === 1 || m === -1) term *= E;
    else if (m === 2 || m === -2) term *= E2;
    sumL += term;
  }

  // 섭동 보정항: A1(금성), A2(목성), L'−F(장동성 항)
  const A1 = norm360(119.75 + 131.849 * T);
  const A2 = norm360(53.09 + 479264.29 * T);
  sumL += 3958 * Math.sin(A1 * DEG) + 1962 * Math.sin((Lp - F) * DEG) + 318 * Math.sin(A2 * DEG);

  // 장동(Δψ) 보정 → 겉보기 황경
  const omega = 125.04452 - 1934.136261 * T;
  const nutation = -0.00478 * Math.sin(omega * DEG);

  return norm360(Lp + sumL / 1e6 + nutation);
}

/** UTC epoch ms 기준 겉보기 달 황경 (도, 0~360). ΔT 보정 포함 */
export function moonLongitudeAtUTCms(utcMs: number): number {
  const year = new Date(utcMs).getUTCFullYear();
  return moonLongitude(jdFromUTCms(utcMs) + deltaTdays(year));
}

/** UTC epoch ms 기준 겉보기 태양 황경 (도, 0~360). ΔT 보정 포함 */
export function sunLongitudeAtUTCms(utcMs: number): number {
  const year = new Date(utcMs).getUTCFullYear();
  return sunLongitude(jdFromUTCms(utcMs) + deltaTdays(year));
}

// ━━━ 달별자리 (Moon Sign) ━━━

/**
 * 달별자리 인덱스 (0=양자리 … 11=물고기자리)
 * @param hourKST 출생 시각 (KST, 0~23). 미제공 시 정오(12시)로 간주.
 *   달은 하루에 약 13° 이동하므로, 시각 미상일 때 정오 기준 오차는 최대 ±6.5° (경계일 출생만 영향)
 */
export function getMoonSignIndex(y: number, m: number, d: number, hourKST?: number): number {
  const h = hourKST === undefined ? 12 : hourKST;
  const lambda = moonLongitudeAtUTCms(kstToUTCms(y, m, d, h));
  return Math.floor(lambda / 30) % 12;
}

// ━━━ 상승궁 (Ascendant) ━━━

/**
 * 그리니치 평균 항성시 GMST (도, 0~360) — Meeus 12장 식 12.4
 * @param jdUT UT 기준 율리우스일 (ΔT 미적용)
 */
function gmstDegrees(jdUT: number): number {
  const T = (jdUT - 2451545.0) / 36525;
  return norm360(
    280.46061837 + 360.98564736629 * (jdUT - 2451545.0) + 0.000387933 * T * T - (T * T * T) / 38710000
  );
}

/**
 * 상승점 황경 (도, 0~360) — 서울 기준
 * λ_asc = atan2( cos θ, −(sin θ·cos ε + tan φ·sin ε) ), θ = 지방 항성시(RAMC)
 */
export function ascendantLongitude(utcMs: number): number {
  const jdUT = jdFromUTCms(utcMs);
  const lst = norm360(gmstDegrees(jdUT) + SEOUL_LON); // 지방 항성시 = GMST + 동경
  const theta = lst * DEG;
  const eps = OBLIQUITY * DEG;
  const phi = SEOUL_LAT * DEG;
  const asc = Math.atan2(Math.cos(theta), -(Math.sin(theta) * Math.cos(eps) + Math.tan(phi) * Math.sin(eps)));
  return norm360(asc / DEG);
}

/**
 * 상승궁 인덱스 (0=양자리 … 11=물고기자리) — 서울(위도 37.57°N, 경도 126.98°E) 기준
 * 상승궁은 약 2시간마다 바뀌므로 출생 시각이 필수이다.
 * @param hourKST 출생 시각 (KST, 0~23). 분 단위 정밀도가 필요하면 minute 전달
 */
export function getRisingSignIndex(y: number, m: number, d: number, hourKST: number, minute: number = 0): number {
  const lambda = ascendantLongitude(kstToUTCms(y, m, d, hourKST, minute));
  return Math.floor(lambda / 30) % 12;
}

