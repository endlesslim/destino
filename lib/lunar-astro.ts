export interface MoonCalcVerification {
  pass: number;
  fail: number;
  details: string[];
}

/** 두 황경 사이 각거리 (0~180°) */
function angularSeparation(a: number, b: number): number {
  const diff = Math.abs(norm360(a - b));
  return diff > 180 ? 360 - diff : diff;
}

/**
 * 달 황경 계산 검증 — korean-lunar-calendar 대조
 *
 * 원리: 음력 1일은 삭(新月, 달-태양 합) 순간이 포함된 날이고, 음력 15일은 망(보름) 부근이다.
 * 삭 순간은 그날 0~24시 어디든 올 수 있고 달은 태양 대비 하루 약 12.2° 이동하므로,
 * 정오 기준 이각은 삭일에 최대 ±7° 안팎, 망일(15일)에 180°±15° 안팎이 정상 범위다.
 * 임계값 25°는 이 자연 변동 폭에 여유를 더한 값 — 계산이 부호/위상 오류 없이 맞는지 판별한다.
 *
 * @returns 2000~2030년 사이 음력 1일 20개(삭) + 음력 15일 20개(망) 검증 결과
 */
export async function verifyMoonCalc(): Promise<MoonCalcVerification> {
  const mod = await import("korean-lunar-calendar");
  const KoreanLunarCalendar = (mod as { default: typeof mod.default }).default ?? mod;

  let pass = 0;
  let fail = 0;
  const details: string[] = [];

  /** 음력 날짜 → 해당 양력일 정오(KST)의 달-태양 이각 검사 */
  const check = (lunarYear: number, lunarMonth: number, lunarDay: number, isFull: boolean): void => {
    const cal = new KoreanLunarCalendar();
    if (!cal.setLunarDate(lunarYear, lunarMonth, lunarDay, false)) {
      fail++;
      details.push(`FAIL 음력 ${lunarYear}-${lunarMonth}-${lunarDay}: 유효하지 않은 음력 날짜`);
      return;
    }
    const solar = cal.getSolarCalendar();
    const noonUTC = kstToUTCms(solar.year, solar.month, solar.day, 12);
    const moonLon = moonLongitudeAtUTCms(noonUTC);
    const sunLon = sunLongitudeAtUTCms(noonUTC);
    const sep = angularSeparation(moonLon, sunLon);

    const ok = isFull ? Math.abs(sep - 180) < 25 : sep < 25;
    const label = isFull ? "망(15일)" : "삭(1일)";
    const target = isFull ? "180°±25°" : "<25°";
    if (ok) {
      pass++;
      details.push(
        `PASS ${label} 음력 ${lunarYear}-${lunarMonth}-${lunarDay} → 양력 ${solar.year}-${solar.month}-${solar.day} 이각 ${sep.toFixed(2)}° (기준 ${target})`
      );
    } else {
      fail++;
      details.push(
        `FAIL ${label} 음력 ${lunarYear}-${lunarMonth}-${lunarDay} → 양력 ${solar.year}-${solar.month}-${solar.day} 이각 ${sep.toFixed(2)}° (기준 ${target})`
      );
    }
  };

  // 2000~2030년 범위에 고르게 분포한 표본: 연도 20개 × (월은 순환 배치)
  for (let i = 0; i < 20; i++) {
    const year = 2001 + Math.floor((i * 29) / 20); // 2001~2028 분포
    const month = ((i * 5) % 12) + 1; // 1~12월 순환
    check(year, month, 1, false); // 삭
  }
  for (let i = 0; i < 20; i++) {
    const year = 2002 + Math.floor((i * 27) / 20); // 2002~2027 분포
    const month = ((i * 7) % 12) + 1;
    check(year, month, 15, true); // 망
  }

  return { pass, fail, details };
}
