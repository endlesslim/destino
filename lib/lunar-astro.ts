//   검증 앵커(Meeus 예제 47.a): 1992-04-12.0 TD → 겉보기 황경 133.1673°
// - 태양 황경: Meeus 25장 저정밀식 + 광행차/장동 보정 (lib/manseryeok.ts와 동일 방식, 순환의존 방지 위해 자체 구현). 오차 ≈ 0.01°
// - 상승궁: GMST(Meeus 12장) → LST(서울 동경 126.98°) → 상승점 황경 공식.
//   황도경사 ε = 23.4367° 고정, 서울 위도 37.57°N 기준
// - 시간대: 입력은 KST(UTC+9) 벽시계 기준. 내부 계산은 UTC epoch ms
// - 12궁 인덱스: 0=양자리(황경 0~30°) … 11=물고기자리 — lib/western.ts ZODIAC 배열과 동일한 순서

const DEG = Math.PI / 180;
const SEOUL_LAT = 37.57; // 서울 위도 (°N)
const SEOUL_LON = 126.98; // 서울 경도 (°E)
const OBLIQUITY = 23.4367; // 황도경사 (°)
const KST_OFFSET_MS = 9 * 3600000; // KST = UTC+9

// ━━━ 시간 변환 ━━━

/** KST 벽시계 날짜/시각 → UTC epoch ms */
export function kstToUTCms(y: number, m: number, d: number, hour: number, minute: number = 0): number {
  return Date.UTC(y, m - 1, d, Math.floor(hour), Math.round((hour % 1) * 60) + minute) - KST_OFFSET_MS;
}

/** UTC epoch ms → 율리우스일 (연속값) */
export function jdFromUTCms(ms: number): number {
  return ms / 86400000 + 2440587.5;
}

/** ΔT(TT−UT) 근사값 (일 단위). 1900~2100 범위에서 오차 ±1분 이내 → 황경 기준 ±0.001° 미만 */
function deltaTdays(year: number): number {
  const t = (year - 2000) / 100;
  const seconds = 64 + 90 * t;
  return seconds / 86400;
}

function norm360(deg: number): number {
  return ((deg % 360) + 360) % 360;
}

// ━━━ 태양 황경 (Meeus 25장 저정밀식) ━━━

/**
 * 겉보기 태양 황경 (도, 0~360)
 * Jean Meeus, Astronomical Algorithms 25장 + 광행차/장동 보정. 오차 ≈ 0.01°
 */
export function sunLongitude(jd: number): number {
  const T = (jd - 2451545.0) / 36525;
  const L0 = 280.46646 + 36000.76983 * T + 0.0003032 * T * T;
  const M = 357.52911 + 35999.05029 * T - 0.0001537 * T * T;
  const C =
    (1.914602 - 0.004817 * T - 0.000014 * T * T) * Math.sin(M * DEG) +
    (0.019993 - 0.000101 * T) * Math.sin(2 * M * DEG) +
    0.000289 * Math.sin(3 * M * DEG);
  const trueLong = L0 + C;
  const omega = 125.04 - 1934.136 * T;
  const apparent = trueLong - 0.00569 - 0.00478 * Math.sin(omega * DEG);
  return norm360(apparent);
}

