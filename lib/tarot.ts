export const FULL_DECK: TarotCard[] = [...MAJOR_ARCANA, ...MINOR_ARCANA];

// ━━━ 데일리 3카드 스프레드 (결정론적) ━━━

function hashString(s: string): number {
  // FNV-1a 32비트 해시
  let h = 0x811c9dc5;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 0x01000193);
  }
  return h >>> 0;
}

export function drawDailySpread(
  year: number,
  month: number,
  day: number,
  dateISO: string
): { past: TarotCard; present: TarotCard; future: TarotCard; isReversed: [boolean, boolean, boolean] } {
  // 생년월일 + 오늘 날짜를 시드로 사용 — 같은 사람, 같은 날 = 같은 결과
  let state = hashString(`${year}-${month}-${day}|${dateISO}`);
  if (state === 0) state = 0x9e3779b9;

  // xorshift32 결정론적 의사난수
  const next = (): number => {
    state ^= (state << 13) >>> 0;
    state >>>= 0;
    state ^= state >>> 17;
    state ^= (state << 5) >>> 0;
    state >>>= 0;
    return state;
  };

  const indices: number[] = [];
  while (indices.length < 3) {
    const idx = next() % FULL_DECK.length;
    if (!indices.includes(idx)) indices.push(idx);
  }

  const isReversed: [boolean, boolean, boolean] = [
    next() % 2 === 1,
    next() % 2 === 1,
    next() % 2 === 1,
  ];

  return {
    past: FULL_DECK[indices[0]],
    present: FULL_DECK[indices[1]],
    future: FULL_DECK[indices[2]],
    isReversed,
  };
}
